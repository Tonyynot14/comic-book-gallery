using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Comic_Book.Models
{
    public class Artist
    {
        public string Name { get; set; }
        public string Role { get; set; }

        public string Info
        {
            get
            {
                return Role + ": " + Name;
            }
        }
    }
}